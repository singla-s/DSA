package JavaBasics;

public class Strings {
    public static void main(String[] args) {

        /*
        Left to right computation
         */
        System.out.println(2 + 3 + "4"); // 54
        System.out.println("2" + 3 + 4); // 234

        System.out.println('2' + 3 + 4); // 57 ? => Ascii equivalent of '2' is 50
    }
}
